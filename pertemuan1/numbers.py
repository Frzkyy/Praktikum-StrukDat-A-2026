#Integer
intA = 67
intB = -309124
intC = -12567890
print(f"{intA} {type(intA)}, {intB} {type(intB)}, {intC} {type(intC)}",end="\n\n")

#Float
fltA = 3.14
fltB = -67.69
fltC = 2e4
print(f"{fltA} {type(fltA)}, {fltB} {type(fltB)}, {fltC} {type(fltC)}",end="\n\n")

#Complex
cpxA = 2j
cpxB = 2+4j
cpxC = -10j
print(cpxA)
print(cpxB)
print(cpxC)




print(f"\n{cpxA} {type(cpxA)}, {cpxB} {type(cpxB)}, {cpxC} {type(cpxC)}",end="\n\n")